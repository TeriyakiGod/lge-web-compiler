int read6502(int address) {
  if (address < RAM_SIZE && address >= 0) {
    return(RAM[address]);
  }
  else if (address == 0xfff9) { //simulated ASCII input 
    return(getchar());
  }
  address = address - 0xE000;
  if (address < 8192 && address >= 0) {
    return(BIOS[address]);
  }
  return(0);
}

void write6502(uint16_t address, uint8_t value) {
  if (address < RAM_SIZE && address >= 0){ 
    RAM[address] = value;
  }
  else if (address == 0xfff8){
    putchar(value);
  }	
}

//a few general functions used by various other functions
void push16(uint16_t pushval) {
    write6502(BASE_STACK + sp, (pushval >> 8) & 0xFF);
    write6502(BASE_STACK + (((sp - 1) & 0xFF)), pushval & 0xFF);
    sp = sp - 2;
}

void push8(uint8_t pushval) {
    write6502(BASE_STACK + sp, pushval);
	sp--;
}

uint16_t pull16() {
    uint16_t temp16;
    temp16 = read6502(BASE_STACK + ((sp + 1) & 0xFF)) | (read6502(BASE_STACK + ((sp + 2) & 0xFF)) << 8);
    sp = sp + 2;
    return(temp16);
}

uint8_t pull8() {
	sp++;
    return read6502(BASE_STACK + sp);
}

void reset6502() {
    pc = read6502(0xFFFC) | (read6502(0xFFFD) << 8);
    a = 0;
    x = 0;
    y = 0;
    sp = 0xFD;
    cpustatus = cpustatus |  FLAG_CONSTANT;
}

void zerocalc(int n) { 
	if (n & 0x00FF) {
		clearzero();
	} else {
		setzero();
	} 
}

void signcalc(int n) { 
	if ((n) & 0x0080) {
		setsign();
	} else {
		clearsign();
	} 
}

void carrycalc(int n) { 
	if ((n) & 0xFF00) {
		setcarry();
	} else {
		clearcarry();
	} 
}

void overflowcalc(int n, int m, int o) { 
	if ((n ^ m) & (n ^ o) & 0x0080){ 
		setoverflow();
	} else {
		clearoverflow();
	} 
}

