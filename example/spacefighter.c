void action(int tz[]){
	int n;
	if(gettimer(6) == 0){
		if(tz[thisstep]){
			settimer(6, tz[thisstep]);
		}
		else{
			if(spritegetvalue(28, S_LIVES) == 0){
				inLevel = 0;
			}
			else{
				settimer(6, 1000);
			}
			return;
		}
		thisstep++;
		n = tz[thisstep];
		thisstep++;
		if(n & 0x000f > 0){
			addMobs(n & 0x000f, 3);
		}
		if(n & 0x00f0 > 0){
			addMobs((n & 0x00f0) >> 4, 2);
		}
		if(n & 0x0f00 > 0){
			addMobs((n & 0x0f00) >> 8, 1);
		}
		if(n & 0xf000 > 0){
			addMobs((n & 0xf000) >> 12, 0);
		}
		drawWay();
	}
}

